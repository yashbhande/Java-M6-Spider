// Define a class named GreaterThan_Operator_Vote_Eligibility
class GreaterThan_Operator_Vote_Eligibility {
    // Main method
    public static void main(String[] args) {
        // Declare and initialize variables representing age and minimum age requirement
        int age = 20;
        int min_age = 18;

        // Compare ages to check eligibility using the greater than operator
        boolean result = age > min_age;

        // Print the result of the comparison
        System.out.println("User is eligible for voting: " + result);
        // Expected output: true
    }
}
