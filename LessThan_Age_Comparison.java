// Define a class named LessThan_Age_Comparison
class LessThan_Age_Comparison {
    // Main method
    public static void main(String[] args) {
        // Declare and initialize variables representing ages
        int age = 20;
        int age2 = 80;

        // Compare ages using the less than operator
        boolean result = age < age2;

        // Print the result of the comparison
        System.out.println("Age is less than 80: " + result);
        // Expected output: true
    }
}
