// This class demonstrates temperature checking using if statements.
public class TemperatureCheckerWithIf {
    public static void main(String[] args) {
        // Temperature value
        int temperature = 25;

        // Checking if temperature is greater than 30
        if (temperature > 30) {
            System.out.println("It's a hot day!");
        }
        if (temperature <= 30) {
            System.out.println("It's a normal day.");
        }
    }
}
